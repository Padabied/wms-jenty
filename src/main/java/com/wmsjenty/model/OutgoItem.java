package com.wmsjenty.model;

public class OutgoItem {
    private int id;
    private String name;
    private int valueForOutgo;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getValueForOutgo() {
        return valueForOutgo;
    }

    public void setValueForOutgo(int valueForOutgo) {
        this.valueForOutgo = valueForOutgo;
    }
}
